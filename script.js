function obtemAtributo() {
  var radioAtributos = document.getElementsByName("atributo");

  for (var i = 0; i < radioAtributos.length; i++) {
    if (radioAtributos[i].checked == true) {
      return radioAtributos[i].value;
    }
  }
}

function duelar() {
  var selecionaAtributo = obtemAtributo();
  var elementoResultado = document.getElementById("resultado");
  var valorCartaJogador = cartaJogador.atributos[selecionaAtributo];
  var valorCartaMaquina = cartaMaquina.atributos[selecionaAtributo];
  const atk = document.getElementById("ataque");

  if (!valorCartaJogador) {
    elementoResultado.innerHTML = "Escolha um atributo";
  } else if (valorCartaJogador > valorCartaMaquina) {
    derrota = derrota - 1000;
    atk.play();
    elementoResultado.innerHTML = "Você destruiu a carta do Inimigo";
    document.getElementById("btnSortear").disabled = false;
    document.getElementById("btnJogar").disabled = true;
  } else if (valorCartaMaquina > valorCartaJogador) {
    vitoria = vitoria - 1000;
    atk.play();
    elementoResultado.innerHTML = "Sua carta foi destruída";
    document.getElementById("btnSortear").disabled = false;
    document.getElementById("btnJogar").disabled = true;
  } else {
    elementoResultado.innerHTML = "As duas cartas foram destruídas";
    vitoria = vitoria - 500;
    derrota = derrota - 500;
    atk.play();
    document.getElementById("btnSortear").disabled = false;
    document.getElementById("btnJogar").disabled = true;
  }
  maoMaquina.splice(sorteiaMaquina, 1);
  maoJogador.splice(sorteiaJogador, 1);
  vitoriaDerrota.innerHTML = vitoria + "  Vs  " + derrota;
  exibirCartaMaquina();
  resultadoDuelo();
}

function resultadoDuelo() {
  var elementoResultado = document.getElementById("resultado");
  if (derrota <= 0 && vitoria <= 0) {
    elementoResultado.innerHTML = "Vocês empataram, foi um Duelo emocionante!";
    document.getElementById("btnSortear").disabled = true;
    document.getElementById("reiniciar").disabled = false;
  } else if (derrota <= 0) {
    elementoResultado.innerHTML = "Você venceu o Duelo!";
    document.getElementById("btnSortear").disabled = true;
    document.getElementById("reiniciar").disabled = false;
  } else if (vitoria <= 0) {
    elementoResultado.innerHTML = "Você perdeu este Duelo!";
    document.getElementById("btnSortear").disabled = true;
    document.getElementById("reiniciar").disabled = false;
  }
}

