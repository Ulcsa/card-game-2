function exibirCartaJogador() {
  var divCartaJogador = document.getElementById("carta-jogador");
  divCartaJogador.style.backgroundImage = `url(${cartaJogador.imagem})`;
  var moldura =
    '<img src="https://ygopro.org/yugioh-card-maker/res/tcg/ygo/border/Normal.png" style=" width: inherit; height: inherit; position: absolute;">';
  var tagHTML = "<div id= 'opcoes' class='carta-status'>";

  var opcoesTexto = "";

  for (var atributo in cartaJogador.atributos) {
    opcoesTexto +=
      "<input type='radio' name='atributo' value='" +
      atributo +
      "'>" +
      atributo +
      " " +
      cartaJogador.atributos[atributo] +
      "<br>";
  }

  var nome = `<p class="carta-subtitle">${cartaJogador.nome}</p>`;
  divCartaJogador.innerHTML = moldura + nome + tagHTML + opcoesTexto + "<div>";
}

function exibirCartaMaquina() {
  var divCartaMaquina = document.getElementById("carta-maquina");
  divCartaMaquina.style.backgroundImage = `url(${cartaMaquina.imagem})`;
  var moldura =
    '<img src="https://ygopro.org/yugioh-card-maker/res/tcg/ygo/border/Normal.png" style=" width: inherit; height: inherit; position: absolute;">';
  var tagHTML = "<div id= 'opcoes' class='carta-status'>";

  var opcoesTexto = "";

  var selecionaAtributo = obtemAtributo();
  var valorCartaMaquina = cartaMaquina.atributos[selecionaAtributo];

  if (!valorCartaMaquina) {
    var divCartaMaquina = document.getElementById("carta-maquina");
    divCartaMaquina.style.backgroundImage = "";
    divCartaMaquina.innerHTML =
      '<img src="https://cdn.shopify.com/s/files/1/1425/8892/products/KON74145--Yu-Gi-Oh-Card-Back-Sleeves-50ct_da77d52d-fbd1-472c-8d87-243660b780ff_grande.jpg?v=1585643869" style=" width: inherit; height: inherit; position: absolute;">';
  } else {
    for (var atributo in cartaMaquina.atributos) {
      opcoesTexto +=
        "<p type='text' name='atributo' value='" +
        atributo +
        "'>" +
        atributo +
        " " +
        cartaMaquina.atributos[atributo];
      +"</p>";
    }

    var nome = `<p class="carta-subtitle">${cartaMaquina.nome}</p>`;
    divCartaMaquina.innerHTML =
      moldura + nome + tagHTML + opcoesTexto + "<div>";
  }
}